# Compiled tillar dasturni oldin mashina tiliga tarjima qilib, ijro etiladigan fayl yaratadi.
# Interpreted tillar esa dasturni bajarilish vaqtida qatorma-qator tarjima qilib bajaradi.
# Compiled tillar tezroq ishlaydi, lekin har bir platforma uchun alohida kompilyatsiya talab qilinadi.
# Interpreted tillar esa platformalarga moslashuvchan, lekin sekinroq ishlaydi.

